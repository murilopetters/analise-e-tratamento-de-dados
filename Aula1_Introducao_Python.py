# Criando uma variável do tipo string
frase = "Python para análise de dados"

# Acessando caracteres
primeiro_caracter = frase[0]
primeiros_10_caracteres = frase[0:10]
primeiros_17_caracteres = frase[:17]

# Imprimindo resultados
print("Primeiro caracter:", primeiro_caracter)
print("Primeiros 10 caracteres:", primeiros_10_caracteres)
print("Primeiros 17 caracteres:", primeiros_17_caracteres)

# Métodos de string
ocorrencias_da_palavra_dados = frase.count("dados")
frase_em_minusculo = frase.lower()
frase_em_maiusculo = frase.upper()
frase_substituida = frase.replace("Python", "Frases")

# Imprimindo resultados
print("Ocorrências da palavra 'dados':", ocorrencias_da_palavra_dados)
print("Frase em minúsculo:", frase_em_minusculo)
print("Frase em maiúsculo:", frase_em_maiusculo)
print("Frase substituída:", frase_substituida)